def count_neighbours(grid, row, col):

    g = lambda x, w: filter(lambda z: 0 <= z < w, map(lambda y: x+y, range(-1, 2)))
    return sum([grid[x][y] for x in g(row, len(grid)) for y in g(col, len(grid[0])) if x != row or y != col])


